import type {
  Franchise,
  FranchiseColors,
  IdentityTransition,
  TeamIdentity,
} from '../types/models';

function colors(
  primary: string,
  secondary: string,
  onPrimary = '#0b1020',
  onSecondary = '#0b1020',
): FranchiseColors {
  return { primary, secondary, onPrimary, onSecondary };
}

function identities(
  rows: Array<{
    city: string;
    teamName: string;
    from: string;
    to?: string;
    abbr?: string;
    transition?: IdentityTransition;
    eraColors?: Partial<FranchiseColors>;
    eraNotes?: string;
  }>,
): TeamIdentity[] {
  return rows.map((r, i) => ({
    id: `${r.city}-${r.teamName}-${i}`.toLowerCase().replace(/[^a-z0-9]+/g, '-'),
    city: r.city,
    teamName: r.teamName,
    fullName: `${r.city} ${r.teamName}`,
    fromSeason: r.from,
    toSeason: r.to,
    abbreviation: r.abbr,
    transitionFromPrior: r.transition ?? (i === 0 ? 'founding' : undefined),
    eraColors: r.eraColors,
    eraNotes: r.eraNotes,
  }));
}

