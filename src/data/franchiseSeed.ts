const po = (
  season: string,
  roundReached?: string,
  result?: string,
): PlayoffAppearanceRecord => ({ season, roundReached, result });

const fin = (season: string, result: 'won' | 'lost', opponent?: string, games?: string): StanleyCupFinalRecord => ({
  season,
  result,
  opponent,
  games,
});

const cup = (season: string, finalsOpponent?: string, connSmytheWinnerId?: string): StanleyCupChampionshipRecord => ({
  season,
  finalsOpponent,
  connSmytheWinnerId,
});

const id = (
  slug: string,
  city: string,
  teamName: string,
  from: string,
  opts: {
    to?: string;
    abbr?: string;
    transition?: IdentityTransition;
    eraColors?: Partial<FranchiseColors>;
    eraNotes?: string;
  } = {},
) => ({
  id: slug,
  city,
  teamName,
  fullName: `${city} ${teamName}`,
  fromSeason: from,
  toSeason: opts.to,
  abbreviation: opts.abbr,
  transitionFromPrior: opts.transition,
  eraColors: opts.eraColors,
  eraNotes: opts.eraNotes,
});

